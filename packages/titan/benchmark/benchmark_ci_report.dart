// ignore_for_file: avoid_print
import 'dart:convert';
import 'dart:io';

// =============================================================================
// Titan CI Benchmark Report Generator
// =============================================================================
//
// Reads JSON benchmark results and appends to docs/BENCHMARKS.md.
// Designed to run in CI after benchmark_ci.dart.
//
// Usage:
//   dart run benchmark/benchmark_ci_report.dart \
//     --json benchmark_results.json \
//     --output ../../docs/BENCHMARKS.md \
//     --commit <sha> \
//     --max-runs 50
//
// =============================================================================

void main(List<String> args) {
  final jsonPath = _getArg(args, '--json') ?? 'benchmark_results.json';
  final outputPath = _getArg(args, '--output') ?? '../../docs/BENCHMARKS.md';
  final commitSha = _getArg(args, '--commit') ?? 'local';
  final maxRuns = int.tryParse(_getArg(args, '--max-runs') ?? '') ?? 50;

  // Read JSON results
  final jsonFile = File(jsonPath);
  if (!jsonFile.existsSync()) {
    print('ERROR: Benchmark results file not found: $jsonPath');
    exit(1);
  }

  final data = jsonDecode(jsonFile.readAsStringSync()) as Map<String, dynamic>;
  final benchmarks = data['benchmarks'] as List<dynamic>;
  final timestamp =
      data['timestamp'] as String? ?? DateTime.now().toIso8601String();

  // Extract date (YYYY-MM-DD HH:MM)
  final date = timestamp.length >= 16
      ? timestamp.substring(0, 16).replaceAll('T', ' ')
      : timestamp;

  // Short commit SHA
  final shortSha = commitSha.length > 7 ? commitSha.substring(0, 7) : commitSha;

  // Get Dart version
  final dartVersion = Platform.version.split(' ').first;

  // Build metric values map (name → formatted value)
  final metrics = <String, String>{};
  for (final b in benchmarks) {
    final m = b as Map<String, dynamic>;
    final name = m['name'] as String;
    final value = (m['value'] as num).toDouble();
    final unit = m['unit'] as String;
    metrics[name] = _formatValue(value, unit);
  }

  // Read or create the markdown file
  final outputFile = File(outputPath);
  _ParsedMarkdown parsed;

  if (outputFile.existsSync()) {
    parsed = _parseMarkdown(outputFile.readAsStringSync(), benchmarks);
  } else {
    // Create fresh
    outputFile.parent.createSync(recursive: true);
    parsed = _ParsedMarkdown(
      metricNames: benchmarks
          .map((b) => (b as Map<String, dynamic>)['name'] as String)
          .toList(),
      rows: [],
    );
  }

  // Compute trend arrows vs previous row
  final trends = <String, String>{};
  if (parsed.rows.isNotEmpty) {
    final lastRow = parsed.rows.last;
    for (final b in benchmarks) {
      final m = b as Map<String, dynamic>;
      final name = m['name'] as String;
      final value = (m['value'] as num).toDouble();
      final unit = m['unit'] as String;
      final prevStr = lastRow[name];
      if (prevStr != null) {
        final prevVal = _parseValue(prevStr);
        if (prevVal != null && prevVal != 0) {
          final pctChange = ((value - prevVal) / prevVal) * 100;
          trends[name] = _trendArrow(pctChange, unit);
        }
      }
    }
  }

  // Build new row
  final newRow = <String, String>{
    '_date': date,
    '_commit': shortSha,
    '_dart': dartVersion,
  };
  for (final entry in metrics.entries) {
    final trend = trends[entry.key] ?? '';
    newRow[entry.key] = trend.isEmpty ? entry.value : '${entry.value} $trend';
  }

  // Add row and trim to max
  parsed.rows.add(newRow);
  while (parsed.rows.length > maxRuns) {
    parsed.rows.removeAt(0);
  }

  // Ensure new metrics are in the column list
  for (final b in benchmarks) {
    final name = (b as Map<String, dynamic>)['name'] as String;
    if (!parsed.metricNames.contains(name)) {
      parsed.metricNames.add(name);
    }
  }

  // Generate markdown output
  final md = StringBuffer();
  md.writeln('# Benchmark History');
  md.writeln();
  md.writeln(
    '> Auto-generated by CI. Last updated: '
    '${DateTime.now().toUtc().toIso8601String().substring(0, 19)}Z',
  );
  md.writeln('>');
  md.writeln(
    '> Tracks the last $maxRuns runs. Oldest entries are pruned automatically.',
  );
  md.writeln();

  // Legend
  md.writeln('## Legend');
  md.writeln();
  md.writeln('| Symbol | Meaning |');
  md.writeln('|--------|---------|');
  md.writeln(r'| ▲ | Regression (>5% worse) |');
  md.writeln(r'| ▼ | Improvement (>5% better) |');
  md.writeln('| ≈ | Within 5% of previous |');
  md.writeln();

  // Summary (latest run)
  md.writeln('## Latest Run');
  md.writeln();
  md.writeln('| Metric | Value |');
  md.writeln('|--------|-------|');
  for (final name in parsed.metricNames) {
    final val = newRow[name] ?? '-';
    md.writeln('| $name | $val |');
  }
  md.writeln();

  // History table
  md.writeln('## History');
  md.writeln();

  // Header row
  final headerCols = ['Date', 'Commit', 'Dart', ...parsed.metricNames];
  md.writeln('| ${headerCols.join(' | ')} |');
  md.writeln('| ${headerCols.map((_) => '---').join(' | ')} |');

  // Data rows (newest first for readability)
  for (final row in parsed.rows.reversed) {
    final cols = <String>[
      row['_date'] ?? '-',
      row['_commit'] ?? '-',
      row['_dart'] ?? '-',
    ];
    for (final name in parsed.metricNames) {
      cols.add(row[name] ?? '-');
    }
    md.writeln('| ${cols.join(' | ')} |');
  }

  md.writeln();

  // Write file
  outputFile.writeAsStringSync(md.toString());
  print('✓ Benchmark report written to $outputPath');
  print('  Runs tracked: ${parsed.rows.length}/$maxRuns');
}

// =============================================================================
// Helpers
// =============================================================================

String _formatValue(double value, String unit) {
  if (unit.contains('/sec') || unit.contains('lookups')) {
    // Large throughput numbers — use compact format
    if (value >= 1e9) return '${(value / 1e9).toStringAsFixed(2)}B $unit';
    if (value >= 1e6) return '${(value / 1e6).toStringAsFixed(2)}M $unit';
    if (value >= 1e3) return '${(value / 1e3).toStringAsFixed(1)}K $unit';
    return '${value.toStringAsFixed(0)} $unit';
  }
  if (unit == 'x' || unit.startsWith('x ')) {
    return '${value.toStringAsFixed(2)}x';
  }
  if (value >= 1000) {
    return '${(value / 1000).toStringAsFixed(2)}ms';
  }
  return '${value.toStringAsFixed(2)} $unit';
}

/// Determines trend arrow based on percentage change.
///
/// For throughput metrics (higher = better): ▲ means regression (went down).
/// For latency metrics (lower = better): ▲ means regression (went up).
String _trendArrow(double pctChange, String unit) {
  const threshold = 5.0;
  final isThroughput =
      unit.contains('/sec') ||
      unit.contains('lookups') ||
      unit == 'x' ||
      unit.startsWith('x ');

  if (pctChange.abs() < threshold) return '≈';

  if (isThroughput) {
    // Higher is better: positive change = improvement
    return pctChange > 0 ? '▼' : '▲';
  } else {
    // Lower is better: positive change = regression
    return pctChange > 0 ? '▲' : '▼';
  }
}

/// Parses a numeric value from a formatted string like "1.23 µs/node" or "4.56M mutations/sec".
double? _parseValue(String formatted) {
  // Strip trend arrows
  var s = formatted
      .replaceAll('▲', '')
      .replaceAll('▼', '')
      .replaceAll('≈', '')
      .trim();

  // Handle compact formats
  final multipliers = {'B': 1e9, 'M': 1e6, 'K': 1e3};

  // Try to extract leading number with optional multiplier
  final match = RegExp(r'^([\d.]+)(B|M|K)?').firstMatch(s);
  if (match == null) return null;

  final numStr = match.group(1)!;
  final suffix = match.group(2);
  var value = double.tryParse(numStr);
  if (value == null) return null;

  if (suffix != null && multipliers.containsKey(suffix)) {
    value *= multipliers[suffix]!;
  }

  // Handle "ms" → convert back to µs
  if (s.contains('ms') && !s.contains('µs')) {
    value *= 1000;
  }

  return value;
}

/// Parses existing BENCHMARKS.md to extract metric names and historical rows.
_ParsedMarkdown _parseMarkdown(
  String content,
  List<dynamic> currentBenchmarks,
) {
  final lines = content.split('\n');
  final metricNames = <String>[];
  final rows = <Map<String, String>>[];

  // Find the history table
  var inHistory = false;
  var headerParsed = false;
  var separatorSkipped = false;

  for (final line in lines) {
    if (line.startsWith('## History')) {
      inHistory = true;
      continue;
    }

    if (!inHistory) continue;

    // Skip empty lines
    if (line.trim().isEmpty) continue;

    // Stop at next section
    if (line.startsWith('## ') || line.startsWith('# ')) break;

    if (!line.startsWith('|')) continue;

    final cells = line
        .split('|')
        .map((c) => c.trim())
        .where((c) => c.isNotEmpty)
        .toList();

    if (!headerParsed) {
      // First table row = header
      // Skip Date, Commit, Dart — rest are metric names
      if (cells.length > 3) {
        metricNames.addAll(cells.sublist(3));
      }
      headerParsed = true;
      continue;
    }

    if (!separatorSkipped) {
      // Second row is separator (---)
      separatorSkipped = true;
      continue;
    }

    // Data row — rows are newest-first in the file, so we insert at 0
    // to restore chronological order
    if (cells.length >= 3) {
      final row = <String, String>{
        '_date': cells[0],
        '_commit': cells[1],
        '_dart': cells[2],
      };
      for (var i = 3; i < cells.length && (i - 3) < metricNames.length; i++) {
        row[metricNames[i - 3]] = cells[i];
      }
      rows.insert(0, row);
    }
  }

  // If no metrics found from file, use current benchmark names
  if (metricNames.isEmpty) {
    metricNames.addAll(
      currentBenchmarks.map(
        (b) => (b as Map<String, dynamic>)['name'] as String,
      ),
    );
  }

  return _ParsedMarkdown(metricNames: metricNames, rows: rows);
}

String? _getArg(List<String> args, String name) {
  final idx = args.indexOf(name);
  if (idx != -1 && idx + 1 < args.length) return args[idx + 1];
  return null;
}

class _ParsedMarkdown {
  final List<String> metricNames;
  final List<Map<String, String>> rows;

  _ParsedMarkdown({required this.metricNames, required this.rows});
}
